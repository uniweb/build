/**
 * S3 + CloudFront host adapter
 *
 * Targets AWS-hosted static sites fronted by CloudFront with an S3 origin
 * accessed via Origin Access Control (OAC). The challenge with this setup
 * is that S3 REST endpoints (the kind OAC requires) do not auto-resolve
 * directory indexes — a request for `/about` returns 404 even if
 * `about/index.html` exists. This adapter solves it by shipping a
 * CloudFront Function (viewer-request) that rewrites the URI before the
 * cache lookup.
 *
 * postBuild: writes
 *   dist/cloudfront-function.js          — the URI rewrite source
 *   dist/.uniweb-deploy-manifest.json    — what the deploy step will do
 *   (and removes dist/_redirects if a netlify-shaped run left one behind)
 *
 * deploy: subprocesses `aws` CLI to
 *   1. aws s3 sync dist/assets/  → s3://<bucket>/assets/  (immutable cache)
 *   2. aws s3 sync dist/         → s3://<bucket>/         (short cache, --delete)
 *   3. aws cloudfront create-invalidation
 *
 * Provisioning the bucket, distribution, ACM cert, and uploading the
 * CloudFront Function itself are out of scope (one-time IaC). The first
 * deploy explains what the user still needs to do; subsequent deploys
 * just push files and invalidate.
 */

import { writeFile, unlink, readFile } from 'node:fs/promises'
import { existsSync } from 'node:fs'
import { join } from 'node:path'
import { spawn } from 'node:child_process'

const FUNCTION_SOURCE = `// CloudFront Function — viewer-request — directory-index resolution.
//
// Attach to the default cache behavior of your CloudFront distribution.
// Without this, requests for /about (or /about/) return 404 because
// S3 REST endpoints don't auto-resolve directory indexes.
//
// Generated by @uniweb/build's s3-cloudfront adapter.
function handler(event) {
    var request = event.request;
    var uri = request.uri;
    if (uri.endsWith('/')) {
        request.uri = uri + 'index.html';
    } else if (!uri.split('/').pop().includes('.')) {
        request.uri = uri + '/index.html';
    }
    return request;
}
`

const DEFAULT_CACHE_RULES = [
  { match: 'assets/**', cacheControl: 'public, max-age=31536000, immutable' },
  { match: '**',        cacheControl: 'public, max-age=60, must-revalidate' },
]

const DEFAULT_INVALIDATION_PATHS = [
  '/',
  '/index.html',
  '/*.html',
  '/*/index.html',
  '/*/*/index.html',
  '/sitemap.xml',
  '/robots.txt',
  '/_pages/*',
]

const FUNCTION_FILE     = 'cloudfront-function.js'
const MANIFEST_FILE     = '.uniweb-deploy-manifest.json'
const REDIRECTS_FILE    = '_redirects'

/**
 * Detect whether the build inlined the foundation (standalone) or
 * left it to be loaded by URL at runtime (linked).
 *
 * The user's site.yml `foundation:` declaration is the only thing that
 * controls this — `uniweb build` never registers or ships foundation code,
 * so the rule is purely the shape of the `foundation:` ref:
 *   - registry ref ('@ns/name@ver'), https:// URL, or { url } object → linked
 *   - everything else (file: ref, workspace path) → standalone
 */
function detectFoundationMode(siteContent) {
  const foundation = siteContent?.config?.foundation
  if (!foundation) {
    return { shape: 'standalone', foundation: null, url: null }
  }
  if (typeof foundation === 'string') {
    const isRegistryRef = /^@[a-z0-9_-]+\/[a-z0-9_-]+@.+$/.test(foundation)
    const isUrl = /^https?:\/\//.test(foundation)
    if (isRegistryRef || isUrl) {
      return { shape: 'linked', foundation, url: isUrl ? foundation : null }
    }
    return { shape: 'standalone', foundation, url: null }
  }
  if (foundation && typeof foundation === 'object' && foundation.url) {
    return { shape: 'linked', foundation: foundation.ref || null, url: foundation.url }
  }
  return { shape: 'standalone', foundation: null, url: null }
}

/**
 * Build the deploy manifest. Persisted to dist/ so the artifact is
 * self-describing — anyone with dist/ can read this file to learn
 * cache rules, invalidation patterns, and whether the site has a
 * runtime cross-origin dependency on the foundation URL (linked) or
 * is fully self-contained (standalone).
 *
 * postBuild has no access to deploy.yml, so config-shaped fields
 * (bucket, distributionId, region, cacheRules, invalidationPaths)
 * land null at build time. The deploy hook augments the manifest
 * with the resolved target's config before uploading dist/.
 *
 * `ciContext` (detect-ci-context.js output, or null) is recorded
 * verbatim so the manifest captures *where the artifact was built* —
 * CI runner, branch, sha, isProduction signal. Useful for debugging
 * a deployed bundle and for future audit / propagation use cases.
 */
function buildManifest(siteContent = null, ciContext = null) {
  return {
    host: 's3-cloudfront',
    generatedAt: new Date().toISOString(),
    bucket: null,
    distributionId: null,
    region: null,
    foundationMode: detectFoundationMode(siteContent),
    cacheRules: null,
    invalidationPaths: null,
    cloudfrontFunctionFile: FUNCTION_FILE,
    ciContext,
    notes: [
      'Upload cloudfront-function.js to your CloudFront distribution as a viewer-request function on the default cache behavior.',
      'See https://docs.aws.amazon.com/AmazonCloudFront/latest/DeveloperGuide/cloudfront-functions.html',
    ],
  }
}

/**
 * Drop helper files into dist/.
 *
 * Also removes dist/_redirects if a previous run with a different
 * adapter left one behind — defense in depth so the same dist/ never
 * carries conflicting host outputs.
 */
async function postBuild({ distDir, siteContent, ciContext = null, onProgress = () => {} }) {
  await writeFile(join(distDir, FUNCTION_FILE), FUNCTION_SOURCE)
  onProgress(`Wrote ${FUNCTION_FILE} (CloudFront Function source)`)

  const manifest = buildManifest(siteContent, ciContext)
  await writeFile(join(distDir, MANIFEST_FILE), JSON.stringify(manifest, null, 2))
  onProgress(`Wrote ${MANIFEST_FILE}`)
  onProgress(`Site mode: ${manifest.foundationMode.shape}` + (manifest.foundationMode.shape === 'linked' && manifest.foundationMode.url ? ` (foundation loaded from ${manifest.foundationMode.url})` : ''))

  const stale = join(distDir, REDIRECTS_FILE)
  if (existsSync(stale)) {
    await unlink(stale)
    onProgress(`Removed stale ${REDIRECTS_FILE} (Netlify-shaped, not used by CloudFront)`)
  }
}

/**
 * Update the manifest written at build time with config from the
 * resolved deploy.yml target. Called from the deploy hook before
 * upload so the artifact-on-disk reflects what was actually deployed.
 */
async function augmentManifest(distDir, deployConfig) {
  const path = join(distDir, MANIFEST_FILE)
  let manifest
  try {
    manifest = JSON.parse(await readFile(path, 'utf8'))
  } catch {
    return // Build did not write a manifest — nothing to augment.
  }
  manifest.bucket = deployConfig.bucket ?? manifest.bucket
  manifest.distributionId = deployConfig.distributionId ?? manifest.distributionId
  manifest.region = deployConfig.region ?? manifest.region
  manifest.cacheRules = deployConfig.cacheRules || DEFAULT_CACHE_RULES
  manifest.invalidationPaths = deployConfig.invalidationPaths || DEFAULT_INVALIDATION_PATHS
  manifest.deployedAt = new Date().toISOString()
  await writeFile(path, JSON.stringify(manifest, null, 2))
}

/* ------------------------------------------------------------------ *
 * Deploy hook                                                        *
 * ------------------------------------------------------------------ */

class DeployError extends Error {
  constructor(message, { hint } = {}) {
    super(message)
    this.name = 'DeployError'
    this.hint = hint
  }
}

/**
 * Run an `aws` subcommand with stdout piped to the user. stderr is
 * captured so we can translate common AWS errors into actionable hints.
 *
 * @returns {Promise<{stdout: string, stderr: string}>}
 * @throws {DeployError} when aws exits non-zero, with a translated hint.
 */
function runAws(args, { env, log }) {
  return new Promise((resolve, reject) => {
    const child = spawn('aws', args, {
      env,
      stdio: ['ignore', 'pipe', 'pipe'],
    })

    let stdout = ''
    let stderr = ''
    child.stdout.on('data', (chunk) => {
      const s = chunk.toString()
      stdout += s
      log(s.replace(/\n$/, ''))
    })
    child.stderr.on('data', (chunk) => { stderr += chunk.toString() })

    child.on('error', (err) => {
      // ENOENT = aws CLI not on PATH.
      if (err.code === 'ENOENT') {
        reject(new DeployError(
          'The AWS CLI (`aws`) is not installed or not on PATH.',
          {
            hint: [
              'Install: https://docs.aws.amazon.com/cli/latest/userguide/getting-started-install.html',
              '  macOS:    brew install awscli',
              '  Linux:    https://aws.amazon.com/cli/',
              '  Windows:  https://aws.amazon.com/cli/',
              '',
              'Then configure credentials with `aws configure` (or set AWS_ACCESS_KEY_ID, AWS_SECRET_ACCESS_KEY, AWS_REGION env vars).',
            ].join('\n'),
          }
        ))
        return
      }
      reject(err)
    })

    child.on('close', (code) => {
      if (code === 0) {
        resolve({ stdout, stderr })
        return
      }
      reject(translateAwsError(code, stderr, args))
    })
  })
}

/**
 * Map common aws CLI failures into DeployError with a helpful hint.
 * Falls through to a generic error with the raw stderr.
 */
function translateAwsError(exitCode, stderr, args) {
  const subcommand = args.slice(0, 2).join(' ')
  const out = stderr.trim()

  // Credentials problems
  if (/Unable to locate credentials/i.test(out) || /credentials not found/i.test(out)) {
    return new DeployError(
      `aws ${subcommand} failed: AWS credentials not found.`,
      {
        hint: [
          'AWS looks for credentials in this order:',
          '  1. Environment variables (AWS_ACCESS_KEY_ID, AWS_SECRET_ACCESS_KEY)',
          '  2. ~/.aws/credentials  (configured by `aws configure`)',
          '  3. EC2/ECS/SSO instance profile',
          '',
          'If you use named profiles, set `deploy.profile` in site.yml or AWS_PROFILE in your shell.',
        ].join('\n'),
      }
    )
  }

  if (/ExpiredToken|InvalidClientTokenId/i.test(out)) {
    return new DeployError(
      `aws ${subcommand} failed: AWS credentials expired or invalid.`,
      { hint: 'Refresh your session (e.g., `aws sso login` or rotate keys), then retry.' }
    )
  }

  if (/AccessDenied|not authorized/i.test(out)) {
    return new DeployError(
      `aws ${subcommand} failed: the configured IAM principal lacks required permissions.`,
      {
        hint: [
          'The deploy needs:',
          '  s3:PutObject, s3:DeleteObject, s3:ListBucket on the bucket',
          '  cloudfront:CreateInvalidation on the distribution',
          '',
          'Original error:',
          out.split('\n').slice(0, 3).join('\n'),
        ].join('\n'),
      }
    )
  }

  // Bucket / distribution missing
  if (/NoSuchBucket/i.test(out)) {
    return new DeployError(
      `aws ${subcommand} failed: the configured S3 bucket does not exist.`,
      { hint: 'Verify `bucket` and `region` on the resolved deploy.yml target.' }
    )
  }

  if (/NoSuchDistribution|InvalidArgument.*distribution/i.test(out)) {
    return new DeployError(
      `aws ${subcommand} failed: the configured CloudFront distribution was not found.`,
      { hint: 'Verify `deploy.distributionId` in site.yml.' }
    )
  }

  // Network / region trouble
  if (/Could not connect|EndpointConnectionError/i.test(out)) {
    return new DeployError(
      `aws ${subcommand} failed: cannot reach AWS endpoints.`,
      { hint: 'Check network connectivity and that `deploy.region` is a valid AWS region.' }
    )
  }

  // Generic fallback — surface the raw output.
  return new DeployError(
    `aws ${subcommand} failed (exit ${exitCode}).`,
    { hint: out ? `Original error:\n${out}` : 'No error output captured.' }
  )
}

/**
 * Validate the resolved deploy.yml target. Throws DeployError listing
 * every missing field at once (so the user fixes the whole block in
 * one pass instead of trial-and-error).
 *
 * When ALL required fields are missing (cold-start), the error includes
 * a one-time AWS setup walkthrough so the user knows what to provision
 * before filling in deploy.yml. When only some are missing (partial),
 * the error stays terse — assume the user already provisioned and just
 * needs to finish the config.
 */
function validateDeployConfig(deployConfig, distDir) {
  const missing = []
  if (!deployConfig.bucket)         missing.push('bucket')
  if (!deployConfig.distributionId) missing.push('distributionId')
  if (!deployConfig.region)         missing.push('region')

  if (missing.length === 0) return

  const isColdStart = missing.length === 3

  // Compact message for partial config (user is mid-setup, just finish it).
  if (!isColdStart) {
    throw new DeployError(
      `s3-cloudfront deploy is missing required configuration.`,
      {
        hint: [
          `Add the missing fields to deploy.yml under the resolved target:`,
          ``,
          `  targets:`,
          `    production:`,
          `      host: s3-cloudfront`,
          `      bucket: <your-bucket-name>`,
          `      distributionId: <E1ABC...>`,
          `      region: us-east-1`,
          `      profile: <optional AWS_PROFILE>`,
          ``,
          `Missing: ${missing.join(', ')}`,
        ].join('\n'),
      }
    )
  }

  // Cold-start: deploy.yml has no bucket/distributionId/region yet.
  // The user is on their first deploy attempt — point them at the
  // setup walkthrough rather than dumping a long inline checklist.
  throw new DeployError(
    `s3-cloudfront deploy needs one-time AWS setup before its first run.`,
    {
      hint: [
        `One-time setup walkthrough (S3 bucket, CloudFront distribution,`,
        `IAM user, directory-index Function, custom error responses):`,
        ``,
        `  https://docs.uniweb.app/docs/reference/aws-s3-cloudfront-setup`,
        ``,
        `When done, fill these in deploy.yml next to site.yml:`,
        ``,
        `  default: production`,
        `  targets:`,
        `    production:`,
        `      host: s3-cloudfront`,
        `      bucket: <your-bucket-name>`,
        `      distributionId: <E1ABC...>`,
        `      region: <e.g. ca-central-1>`,
        `      profile: <optional — use if you have multiple AWS profiles>`,
        ``,
        `Then re-run \`uniweb deploy\`. The build artifact in dist/ is ready;`,
        `nothing else to rebuild between attempts.`,
        ``,
        `Prerequisites on this machine:`,
        `  • aws CLI on PATH  (macOS: \`brew install awscli\`)`,
        `  • AWS credentials reachable via env / ~/.aws / SSO / instance role`,
      ].join('\n'),
    }
  )
}

/**
 * Pick a cache-control header for a sync pass. The simplest split:
 * one rule for `assets/**`, another for everything else. Users who
 * need finer control can declare per-pattern rules under their
 * deploy.yml target's cacheRules.
 */
function pickCacheControl(rules, kind) {
  if (kind === 'assets') {
    const r = rules.find(r => r.match === 'assets/**')
    return r ? r.cacheControl : DEFAULT_CACHE_RULES[0].cacheControl
  }
  const r = rules.find(r => r.match === '**')
  return r ? r.cacheControl : DEFAULT_CACHE_RULES[1].cacheControl
}

async function deploy({ distDir, deployConfig = {}, env = process.env, log = () => {} }) {
  validateDeployConfig(deployConfig, distDir)

  // Augment the manifest written at build time with the resolved
  // target's bucket / distId / region / cache rules so the artifact
  // on disk reflects what was actually deployed.
  await augmentManifest(distDir, deployConfig)

  // Read the manifest the postBuild step wrote so the deploy can echo
  // the foundation packaging mode. Best-effort — manifest is optional.
  let manifest = null
  try {
    manifest = JSON.parse(await readFile(join(distDir, MANIFEST_FILE), 'utf8'))
  } catch {
    // No manifest = build was produced by an older toolchain or a
    // non-s3-cloudfront postBuild. Continue without the summary.
  }

  if (manifest?.foundationMode) {
    const { shape, url } = manifest.foundationMode
    if (shape === 'linked' && url) {
      log(`Site mode: linked — foundation loaded at runtime from ${url}`)
      log(`  ⚠ The deployed site has a runtime cross-origin dependency on this URL.`)
      log(`  ⚠ For a fully self-contained site, point site.yml's foundation at a workspace-local file: ref.`)
    } else if (shape === 'linked') {
      log(`Site mode: linked — foundation loaded by URL at runtime`)
    } else {
      log(`Site mode: standalone — foundation inlined into the site bundle`)
    }
  }

  const subprocessEnv = { ...env }
  if (deployConfig.profile) {
    subprocessEnv.AWS_PROFILE = deployConfig.profile
  }
  if (deployConfig.region && !subprocessEnv.AWS_REGION) {
    subprocessEnv.AWS_REGION = deployConfig.region
  }

  const cacheRules = deployConfig.cacheRules || DEFAULT_CACHE_RULES
  const invalidationPaths = deployConfig.invalidationPaths || DEFAULT_INVALIDATION_PATHS

  const bucket = deployConfig.bucket
  const distributionId = deployConfig.distributionId
  const exclude = ['*.uniweb-deploy-*']  // never upload internal manifests

  // Pass 1: assets/ with immutable long cache.
  log(`\n→ Syncing assets/ to s3://${bucket}/assets/ (long cache)`)
  await runAws([
    's3', 'sync',
    join(distDir, 'assets/'),
    `s3://${bucket}/assets/`,
    '--cache-control', pickCacheControl(cacheRules, 'assets'),
    '--delete',
  ], { env: subprocessEnv, log })

  // Pass 2: everything else with short cache. --delete prunes removed
  // pages. assets/ is excluded — it was just synced with different cache
  // headers and we don't want this pass to overwrite them.
  log(`\n→ Syncing dist/ to s3://${bucket}/ (short cache; --delete prunes removed pages)`)
  await runAws([
    's3', 'sync',
    distDir + '/',
    `s3://${bucket}/`,
    '--cache-control', pickCacheControl(cacheRules, 'html'),
    '--delete',
    '--exclude', 'assets/*',
    ...exclude.flatMap(p => ['--exclude', p]),
  ], { env: subprocessEnv, log })

  // Invalidation. Bound the number of paths to CloudFront's default
  // free tier (1000 paths/month) — using path patterns keeps us well
  // under that ceiling.
  log(`\n→ Creating CloudFront invalidation on ${distributionId}`)
  await runAws([
    'cloudfront', 'create-invalidation',
    '--distribution-id', distributionId,
    '--paths', ...invalidationPaths,
  ], { env: subprocessEnv, log })

  log(`\n✓ Deploy complete.`)
  log(`  Bucket:        s3://${bucket}/`)
  log(`  Distribution:  ${distributionId}`)
}

const adapter = {
  name: 's3-cloudfront',
  postBuild,
  deploy,
}

export default adapter

